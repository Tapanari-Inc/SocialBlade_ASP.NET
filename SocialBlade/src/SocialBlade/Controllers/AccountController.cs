        [HttpGet]
        public IActionResult Details(string id)
        {
            id = string.IsNullOrEmpty(id) ? _userManager.GetUserId(User) : id;
            var user = Db.Users.SingleOrDefault(x => x.Id == id);
            if (user != null)
            {
                return View(model: user.Id);
            }
            else
            {
                return View("Error", new ErrorViewModel { ErrorCode = 404, ErrorText = "User not found!" });
            }
        }

        [HttpPost]
        public async Task<dynamic> ToggleFollow(string userId)
        {
            dynamic result = new ExpandoObject();
            ApplicationUser followedUser = await Db.Users.SingleOrDefaultAsync(x => x.Id == userId);
            if (followedUser == null)
                return result.Status = "500";
            ApplicationUser currentUser = await _userManager.GetUserAsync(User);
            var relation = Db.UserRelations
                .Include(x => x.Follower)
                .Include(x => x.Followee)
                .SingleOrDefault(x => x.Follower.Id == currentUser.Id &&
                x.Followee.Id == followedUser.Id);
            if (relation != null)
            {
                Db.UserRelations.Remove(relation);
            }
            else
            {
                Db.UserRelations.Add(new UserRelation
                {
                    Follower = currentUser,
                    Followee = followedUser
                });
            }
            await Db.SaveChangesAsync();
            result.FollowersCount = (Db.UserRelations.Count(x => x.Followee.Id == userId) - 1).Format();
            result.Status = "200";
            return result;
        }
        #region Helpers

        private void AddErrors(IdentityResult result)
        {
            foreach (var error in result.Errors)
            {
                ModelState.AddModelError(string.Empty, error.Description);
            }
        }

        private Task<ApplicationUser> GetCurrentUserAsync()
        {
            return _userManager.GetUserAsync(HttpContext.User);
        }

        private IActionResult RedirectToLocal(string returnUrl)
        {
            if (Url.IsLocalUrl(returnUrl))
            {
                return Redirect(returnUrl);
            }
            else
            {
                return RedirectToAction(nameof(HomeController.Index), "Home");
            }
        }

        #endregion

        //GET: Edit
        [HttpGet]
        public ActionResult Edit()
        {
            var currentUser = Db.Users
                .First(x => x.UserName == User.Identity.Name);

            var editProfileViewModel=new EditProfileViewModel(currentUser);

            return View(editProfileViewModel);
        }

        //POST: Edit
        [HttpPost]
        public ActionResult Edit(EditProfileViewModel profile )
        {
            var currentUser = Db.Users
                .First(x => x.UserName == User.Identity.Name);

            currentUser.FirstName = profile.FirstName;
            currentUser.LastName = profile.LastName;
            currentUser.PhoneNumber = profile.PhoneNumber;
            currentUser.Email = profile.Email;
            currentUser.ProfilePictureUrl = profile.ProfilePictureUrl;

            Db.SaveChanges();

            return RedirectToAction("Details");
        }
